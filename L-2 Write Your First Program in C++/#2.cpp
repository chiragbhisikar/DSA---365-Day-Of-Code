#include <iostream>
using namespace std;

int main()
{
    int a = 2;
    int b = 3;

    cout << "Arithmetic Operator" << endl;
    cout << "a + b = " << a + b << endl;
    cout << "a - b = " << a - b << endl;
    cout << "a * b = " << a * b << endl;
    cout << "a / b = " << a / b << endl;
    cout << "a % b = " << a % b << endl;

    cout << "Comparison Operator" << endl;
    cout << "a == b = " << (a == b) << endl;
    cout << "a < b = " << (a < b) << endl;
    cout << "a > b = " << (a > b) << endl;
    cout << "a <= b = " << (a <= b) << endl;
    cout << "a >= b = " << (a >= b) << endl;

    return 0;
}